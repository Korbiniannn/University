package de.hawlandshut.java1.basics;

public class IncrementExample
{
  public static void increment()
  {
    int i = 0;
    // snippet: increment
    i++;
    // snippet: /increment
  }

  public static void manualIncrement()
  {
    int i = 0;
    // snippet: manualIncrement
    i = i+1;
    // snippet: /manualIncrement
  }

  public static void semiManualIncrement()
  {
    int i = 0;
    // snippet: semiManualIncrement
    i += 1;
    // snippet: /semiManualIncrement
  }
}


